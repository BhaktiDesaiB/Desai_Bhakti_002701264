    private void btnUpdateDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDetailsActionPerformed
        // TODO add your handling code here:
        PatientInitialPage trail = new PatientInitialPage();
        String LoggedPerson = trail.getVariable();
        for(PatientDetails PatientSearch:NewPatientArray){
            if(PatientSearch.getUsername().equals(LoggedPerson)){
                PatientSearch.setFirstName(txtFirstName.getText());
                PatientSearch.setLastName(txtLastName.getText());
                PatientSearch.setAge(Integer.parseInt(txtAge.getText()));
                PatientSearch.setGender(txtGender.getText());
                PatientSearch.setAddress(txtAddress.getText());
                PatientSearch.setPhoneNumber(Double.parseDouble(txtPhoneNumber.getText()));
                PatientSearch.setEmailId(txtEmailID.getText());
                break;
                
            }
            
        }
        PatientCreate.setArrayLst(NewPatientArray);
        JOptionPane.showMessageDialog(this, "Updated Successfully");

    }//GEN-LAST:event_btnUpdateDetailsActionPerformed

    private void txtFirstNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFirstNameKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
	if(Character.isLetter(c)|| Character.isISOControl(c) || Character.isWhitespace(c) )
    {
        txtFirstName.setEditable(true);
    }
    else
    {
        txtFirstName.setEditable(false);
        JOptionPane.showMessageDialog(this, "Invalid Input \nenter valid firstname");
    }
    }//GEN-LAST:event_txtFirstNameKeyPressed

