    private void populateTable() {
        
        System.out.println();
        
        DefaultTableModel table = (DefaultTableModel) tblVitals.getModel();
        table.setRowCount(0);
        System.out.println(history.getHistory().size());
        
        for(VitalSigns vs: history.getHistory()){
                Object[] row = new Object[3];
                row[0] = vs;
                row[1] = vs.getTemperature();
                row[2] = vs.getPulse();
            
                table.addRow(row);
                
        }
    }

