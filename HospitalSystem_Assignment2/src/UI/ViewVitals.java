    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int SelectedIndexRow = tblVitals.getSelectedRow();
        
        if(SelectedIndexRow<0){
            JOptionPane.showMessageDialog(this, "Please select row to Delete");
            return;
        }
        DefaultTableModel table = (DefaultTableModel) tblVitals.getModel();
        VitalSigns SelectedVitals = (VitalSigns) table.getValueAt(SelectedIndexRow, 0);
        
        history.deleteVitals(SelectedVitals);
        
        populateTable();
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

