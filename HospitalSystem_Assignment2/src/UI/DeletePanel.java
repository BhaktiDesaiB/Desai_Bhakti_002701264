    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String SearchUserName = txtSearch.getText();
        Set<String> PatientKeys = PatientDetails.keys();
        Set<String> DoctorKeys = DoctorsDetails.DoctorsKeys();
        Set<String> CAKeys = CommunityAdminDetails.CommunityKeys();
        Set<String> HAKeys = HospitalAdminDetails.HAKeys();
        Set<String> AdminKeys = AdminDetails.AdminKeys();
        
        for(String x:PatientKeys){
            if(SearchUserName.equals(x)){
                txtUserName.setText(x);
                txtPassword.setText(PatientDetails.getValue(x));
                break;
            }
        }
        for(String x:DoctorKeys){
            if(SearchUserName.equals(x)){
                txtUserName.setText(x);
                txtPassword.setText(DoctorsDetails.getDoctorValue(x));
                break;
            }
        }
        for(String x:CAKeys){
            if(SearchUserName.equals(x)){
                txtUserName.setText(x);
                txtPassword.setText(CommunityAdminDetails.getCommunityValue(x));
                break;
            }
        }
        for(String x:HAKeys){
            if(SearchUserName.equals(x)){
                txtUserName.setText(x);
                txtPassword.setText(HospitalAdminDetails.getHAValue(x));
                break;
            }
        }
        for(String x:AdminKeys){
            if(SearchUserName.equals(x)){
                txtUserName.setText(x);
                txtPassword.setText(AdminDetails.getAdminValue(x));
                break;
            }
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String SearchUserName = txtUserName.getText();
        System.out.println(SearchUserName);
        Set<String> PatientKeys = PatientDetails.keys();
        Set<String> DoctorKeys = DoctorsDetails.DoctorsKeys();
        Set<String> CAKeys = CommunityAdminDetails.CommunityKeys();
        Set<String> HAKeys = HospitalAdminDetails.HAKeys();
        Set<String> AdminKeys = AdminDetails.AdminKeys();
        
        for(String x:PatientKeys){
            if(SearchUserName.equals(x)){
                PatientDetails.deletePatientKeyValue(x);
                break;
            }
        }
        for(String x:DoctorKeys){
            if(SearchUserName.equals(x)){
                DoctorsDetails.deleteDoctorKeyValue(x);
                break;
            }
        }
        for(String x:CAKeys){
            if(SearchUserName.equals(x)){
                CommunityAdminDetails.deleteCAKeyValue(x);
                break;
            }
        }
        for(String x:HAKeys){
            if(SearchUserName.equals(x)){
                HospitalAdminDetails.deleteHAKeyValue(x);
                break;
            }
        }
        for(String x:AdminKeys){
            if(SearchUserName.equals(x)){
                AdminDetails.deleteAdminKeyValue(x);
                break;
            }
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

